public class Aula01 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Constantes - inteiras, reais e String
        // Comando e saida de dados
        //Comando de saida - Como escrever na tela
        System.out.println("Meu Primeiro Programa em Java");
        System.out.println("Podemos usar Símbolos: * & % $ # @ !");
        System.out.println("Podemos usar Números: 1 2 3 4 5  7.89");
        //Como escrever um numero inteiro na tela
        System.out.println("Numero Inteiro: " + 10);
        //Como escrever um numero real na tela
        System.out.println("Número Real: " + 10.98765);
        //Saida formatada de uma constante real
        System.out.printf("Numero Real: %.2f",10.1234567);
        System.out.println();
    }
}
